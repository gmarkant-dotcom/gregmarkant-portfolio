export function getEventProjects() {
  return projects
    .filter((p) => p.workExamplesPage === 'events')
    .sort((a, b) => {
      const yearA = parseInt(String(a.year).split(',')[0].trim())
      const yearB = parseInt(String(b.year).split(',')[0].trim())
      return yearB - yearA
    })
}
